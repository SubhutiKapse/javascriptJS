//logical operator
//AND---&&
// true && true----true
// false && true----false
// true && false----false
// false && false----false

console.log(2==2 && 3==3)
console.log(2==6 && 3==3)
console.log(2==2 && 3==9)
console.log(2==0 && 3==5)

//OR--||
// true || true----true
// false || true----true
// true || false----true
// false || false----false
console.log(2==2 ||3==3)
console.log(2==6 || 3==3)
console.log(2==2 || 3==9)
console.log(2==0 || 3==5)


//NOT--!
//!true--false
//!false--true
console.log(!(2==3))
console.log(!(2==2))
